/*VISUALIZAR LAS UNIDADES TOTALES VENDIDAS DE
CADA COCHE A CADA CLIENTE, MOSTRANDO
EL NOMBRE DEL PRODUCTO,NUMERO DE CLIENTE Y LA SUMA DE UNIDADES*/


SELECT O.MODELO AS 'MODELO',O.MARCA AS 'MARCA',C.NOMBRE AS 'NOMBRE CLIENTE',SUM(CANTIDAD)
FROM CLIENTE C 
INNER JOIN ENCARGO E ON E.CLIENTE_ID = C.ID
INNER JOIN COCHE O ON E.COCHE_ID = O.ID
GROUP BY E.COCHE_ID,E.CLIENTE_ID;
