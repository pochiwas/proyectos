/*LISTAR TODOS LOS ENCARGOS REALIZADOS 
CON LAS MARCA DEL COCHE Y EL NOMBRE DEL CLIENTE*/

SELECT E.ID,E.CANTIDAD,C.MARCA,C.MODELO,CL.NOMBRE
FROM ENCARGO E,COCHE C,CLIENTE CL
WHERE E.COCHE_ID = C.ID
AND E.CLIENTE_ID = CL.ID
ORDER BY E.CANTIDAD DESC;


/*OTRA SOLUCION*/

SELECT E.ID,CO.MARCA,CO.MODELO,C.NOMBRE
FROM ENCARGO E
INNER JOIN COCHE CO ON CO.ID = E.COCHE_ID
INNER JOIN CLIENTE C ON C.ID = E.CLIENTE_ID;
