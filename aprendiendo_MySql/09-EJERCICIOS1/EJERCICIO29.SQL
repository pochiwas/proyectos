/*CREAR UNA VISTA LLAMADA VENDEDORES A 
QUE INCLUIRA TODOS LOS VENDEDORES DEL GRUPO 
QUE SE LLAME VENDEDORES A */

CREATE VIEW vendedoresA AS
SELECT * 
FROM VENDEDOR 
WHERE GRUPO_ID IN (SELECT ID
                   FROM GRUPO
                   WHERE NOMBRE='VENDEDORES A');

                   SELECT * FROM vendedoresA;