/*OBTENER LISTADO DE CLIENTES ATENDIDOS
POR EL VENDEDOR DAVID LOPEZ*/

SELECT * 
FROM CLIENTE C 
WHERE C.VENDEDOR_ID IN (SELECT ID 
                       FROM VENDEDOR
                       WHERE NOMBRE ='DAVID'
                       AND APELLIDO='LOPEZ');

UPDATE CLIENTE
SET VENDEDOR_ID=2
WHERE ID =6;

