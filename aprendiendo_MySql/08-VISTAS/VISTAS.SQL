CREATE VIEW ENTRADAS_CON_NOMBRES AS
SELECT E.ID,E.TITULO,U.NOMBRE AS "Autor" ,C.NOMBRE AS "Categoria"
FROM ENTRADA E
INNER JOIN USUARIO U ON E.USUARIO_ID = U.ID
INNER JOIN CATEGORIA C ON E.CATEGORIA_ID = C.ID;

