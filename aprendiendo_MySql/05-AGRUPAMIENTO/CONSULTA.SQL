SELECT COUNT(TITULO) AS 'NUMERO DE ENTRADAS',CATEGORIA_ID FROM ENTRADA GROUP BY CATEGORIA_ID;

/*CONSULTAS DE AGRUPAMIENTO CON CONDICIONES*/

SELECT COUNT(TITULO) AS 'NUMERO DE ENTRADAS', CATEGORIA_ID 
FROM ENTRADA GROUP BY CATEGORIA_ID HAVING COUNT(TITULO) >= 4;

/*SACANDO LA MEDIA, APROXIMADO*/
SELECT AVG(ID) AS 'MEDIA DE ENTRADAS' FROM ENTRADA;
